class Appointment:

    def set_doctor(self, doctor):
        self.docto = doctor

    def set_patient(self, patient):
        self.patien = patient

    def set_date(self, date):
        self.date = date

    def get_doctor(self):
        return self.docto

    def get_patient(self):
        return self.patien

    def get_date(self):
        return self.date
